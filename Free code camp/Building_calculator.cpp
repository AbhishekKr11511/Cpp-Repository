#include<iostream>
using namespace std;

int main()
{
    int num1, num2;
    cout<<"Enter first number = ";
    cin>>num1;
    cout<<"Enter second number = ";
    cin>>num2;
    cout<< num1 + num2<<endl;   // All operations will give integer values as num1 and num2 are int values
    cout<< num1 - num2<<endl;
    cout<< num1 * num2<<endl;
    cout<< num1 / num2<<endl;
    cout<< num1 % num2<<endl;

    return 0;
}